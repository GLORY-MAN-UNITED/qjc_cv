function renderLinks(links = []) {
  if (!links.length) {
    return "";
  }

  return `
    <div class="showcase-links">
      ${links
        .map(
          (link) => `
            <a class="inline-link" href="${link.href}" target="_blank" rel="noreferrer">
              ${link.label}
            </a>
          `
        )
        .join("")}
    </div>
  `;
}

